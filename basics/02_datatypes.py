# Typecasting in Python

a = 10
b = 3.0
result = a + b  # Implicit casting: int + float = float

x = int(3.8)     # Java: int x = (int) 3.8;
y = str(123)     # Java: String y = Integer.toString(123);

print(result, x, y)
# Output: 13.0 3 123


