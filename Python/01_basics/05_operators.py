a, b = 10, 5

# Arithmetic Operators
print(a + b, a - b, a * b, a / b, a % b)  # Java: + - * / %
# Output: 15 5 50 2.0 0

# Relational Operators
print(a == b, a != b, a > b, a < b)       # Java: == != > <
# Output: False True True False

# Logical Operators
print(True and False, True or False, not False)  # Java: && || !
# Output: False True True